# birds 딕셔너리로 짹, 날기 저장
birds = {
    "앵무새": {"짹":"앵무","날기":"날고 있습니다"},
    "참새" : {"짹":"포로롱", "날기":"잠깐 날았습니다"},
    "비둘기" : {"짹":"구구", "날기":"날 수 있을까요?"},
    "닭" : {"짹":"꼬꼬", "날기":"나는게 쉽지 않아보입니다"},
    "러버덕" : {"짹":"꽉" , "날기":"날 수 없습니다"}
}

bird, action = input("새와 행동을 입력하세요:").split(",")

if action == "짹" :
    print(f"{bird}(이)가 '{birds[bird][action]}' 울음소리를 냈습니다 ")
elif action =="날기":
    print(f"{bird}(이)가 {birds[bird][action]}")
else :
    print("잘못된입력입니다")
